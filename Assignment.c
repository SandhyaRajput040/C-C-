#include<Stdio.h>
void main(){

int num1=10;
int num2=20;
 
 num1=num2;//equal
printf("%d\n",num1);
 num1+=num2;//num1=num1+num2  plus equals
printf("%d\n",num1);
 num1-=num2;//num1=num1-num2  minus equals
printf("%d\n",num1);
 num1*=num2;//num1=num1*num2  multiple equals
printf("%d\n",num1);
 num1/=num2;//num1=num1/num2  divide equals
printf("%d\n",num1);
 num1%=num2;//num1=num1%num2   reminder equals 
printf("%d\n",num1);
 
 
}
